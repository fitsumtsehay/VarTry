
public class VarTry {
    public static void main(String[] argv)
    {
        int ATestScore;                    // declares integer variable named ATestScore
        ATestScore = 50;                    // gives ATestScore the value 9
        System.out.println("Test Score initialized is:" +ATestScore);    // print the value of ATestScore to the screen

        double PriceTag;                    // declares double variable named PriceTag
        PriceTag = 24.99;                    // gives PriceTag the value 24.99
        System.out.println("Price tag:" +PriceTag);    // print the value of Price Tag to the screen


        String a2 = "Bobby Johnson";                      // declares the string a2
        System.out.println("Username:" +a2);            //  print the value of a2 to the screen
    }
}
